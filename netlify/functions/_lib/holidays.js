function pad2(n) {
  return String(n).padStart(2, "0");
}

// year, month0（0始まり）, date を受け取り、日本の祝日かを判定。
function isJapaneseHoliday(year, month0, date) {
  return JP_HOLIDAYS.has(`${year}-${pad2(month0 + 1)}-${pad2(date)}`);
}

module.exports = { isJapaneseHoliday, JP_HOLIDAYS };
